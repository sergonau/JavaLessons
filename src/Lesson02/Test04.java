/* Четные числа
Используя цикл for вывести на экран чётные числа от 1 до 100 включительно.
Через пробел либо с новой строки.
*/

public class Test04 {

    public static void main(String[] args) {

        int a = 0;
        for(int i = 0; i < 50; i++){
            a += 2;
            System.out.println(a);
        }

    }

}
